const color = 'red';
switch(color){
    case 'green':
        console.log('it is green')
        break
    case 'white':
        console.log('it is white')
        break
    case 'black':
        console.log('it is black')
        break
    case 'blue':
        console.log('it is blue')
        break
    case 'red':
        console.log('it is red')
        break
    default:
        console.log('it is yellow')
}